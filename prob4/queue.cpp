#include <iostream>
using namespace std;

class Queue {
public:
    int arr[5];
    int front = -1, rear = -1;

    void enqueue(int x) {
        if (rear == 4) {
            cout << "Queue Full\n";
            return;
        }
        if (front == -1) front = 0;
        arr[++rear] = x;
    }

    void dequeue() {
        if (front == -1 || front > rear) {
            cout << "Queue Empty\n";
            return;
        }
        cout << "Removed: " << arr[front++] << endl;
    }

    void display() {
        if (front == -1) {
            cout << "Empty\n";
            return;
        }
        for (int i = front; i <= rear; i++)
            cout << arr[i] << " ";
    }
};

int main() {
    Queue q;
    q.enqueue(10);
    q.enqueue(20);
    q.enqueue(30);
    q.display();
    cout << endl;

    q.dequeue();
    q.display();
    return 0;
}
